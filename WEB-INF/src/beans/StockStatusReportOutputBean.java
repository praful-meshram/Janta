package beans;

public class StockStatusReportOutputBean {
	
	private String item_name;
	private String item_group_desc;
	private String item_rate;
	private String item_mrp;
	private String item_site_qty;
	private String site_name;
	private String checkBox;
	
	
	
	public String getCheckBox() {
		return checkBox;
	}
	public void setCheckBox(String checkBox) {
		this.checkBox = checkBox;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getItem_group_desc() {
		return item_group_desc;
	}
	public void setItem_group_desc(String item_group_desc) {
		this.item_group_desc = item_group_desc;
	}
	public String getItem_rate() {
		return item_rate;
	}
	public void setItem_rate(String item_rate) {
		this.item_rate = item_rate;
	}
	public String getItem_mrp() {
		return item_mrp;
	}
	public void setItem_mrp(String item_mrp) {
		this.item_mrp = item_mrp;
	}
	public String getItem_site_qty() {
		return item_site_qty;
	}
	public void setItem_site_qty(String item_site_qty) {
		this.item_site_qty = item_site_qty;
	}
	public String getSite_name() {
		return site_name;
	}
	public void setSite_name(String site_name) {
		this.site_name = site_name;
	}
	
	
}
