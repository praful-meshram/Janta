package beans;

public class ReorderListOutputBean {
	private String itemCode;
	private String itemName;
	private String itemWeight;
	private String reorderQty;
	private String StoreQty;
	
	public String getItemCode() {
		return itemCode;
	}
	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemWeight() {
		return itemWeight;
	}
	public void setItemWeight(String itemWeight) {
		this.itemWeight = itemWeight;
	}
	public String getReorderQty() {
		return reorderQty;
	}
	public void setReorderQty(String reorderQty) {
		this.reorderQty = reorderQty;
	}
	public String getStoreQty() {
		return StoreQty;
	}
	public void setStoreQty(String storeQty) {
		StoreQty = storeQty;
	}
	
	

}
